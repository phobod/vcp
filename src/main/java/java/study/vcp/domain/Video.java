package java.study.vcp.domain;

public class Video {
	private String title;
	private String description;
	private String linkToThumbnail;
	private String linkToVideo;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getLinkToThumbnail() {
		return linkToThumbnail;
	}
	public void setLinkToThumbnail(String linkToThumbnail) {
		this.linkToThumbnail = linkToThumbnail;
	}
	public String getLinkToVideo() {
		return linkToVideo;
	}
	public void setLinkToVideo(String linkToVideo) {
		this.linkToVideo = linkToVideo;
	}

}
