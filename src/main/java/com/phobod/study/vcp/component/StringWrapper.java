package com.phobod.study.vcp.component;

public class StringWrapper {
	private String value;

	public StringWrapper() {
		super();
	}

	public StringWrapper(String value) {
		super();
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
